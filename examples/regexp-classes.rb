# coding: utf-8
/\A[^1-9][xX][[:xdigit:]][[:space:]] = [[:digit:]]{2}\Z/ =~ '0xf = 15'
chars = (200..3000).map{|c| c.chr 'utf-8'}
chars.grep(/[[:digit:]]/).first(10)
#=> ["٠", "١", "٢", "٣", "٤", "٥", "٦", "٧", "٨", "٩"]
chars.grep(/[[:upper:]]/).first(10)
#=> ["È", "É", "Ê", "Ë", "Ì", "Í", "Î", "Ï", "Ð", "Ñ"]
chars.grep(/[[:lower:]]/).first(10)
#=> ["ß", "à", "á", "â", "ã", "ä", "å", "æ", "ç", "è"]
